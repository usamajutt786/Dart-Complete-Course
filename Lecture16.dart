//do..while loop
//The body of the loop will be executed first and then the condition is tested.
//syntax
//                   do{
//                      text expression;
//                        Body of loop
//                      }while(condition);

void main()
{
  var a=0;
  do
  {
    print('hello and the value of a is :$a');
    a++;
  } while (a<10);
}