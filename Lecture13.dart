//for…in loop
//For…in loop in Dart takes an expression or object as an iterator. It is similar to that in Java
//and its execution flow is also the same as that in Java
//syntax
//       for (var in expression) {
//             // Body of loop
//                }

void main()
{
  var arr=[10,20,30,40,50];
  
   for (var i in arr) {
     print(i);
   }
}